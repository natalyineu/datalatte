// ─── KEYWORD → POOL MAPPING ────────────────────────────────────────────────
function detectPool(slug, category) {
  const text = (slug + " " + category).toLowerCase();
  if (/coffee|cafe|espresso|latte|barista/.test(text)) return "coffee";
  if (/salon|hair|barber|nail|beauty|spa|grooming.*hair/.test(text)) return "salon";
  if (/pet|dog|cat|groom|animal|vet/.test(text)) return "pets";
  if (/fitness|gym|yoga|workout|exercise|pilates|class/.test(text)) return "fitness";
  if (/podcast|audio|spotify|radio|sound/.test(text)) return "audio";
  if (/video|ctv|ott|streaming|youtube|tiktok|reels/.test(text)) return "video";
  if (/social|instagram|facebook|meta|twitter|linkedin/.test(text)) return "social";
  if (/ai|chatbot|automation|robot|machine/.test(text)) return "ai";
  if (/email|sms|newsletter|inbox|message/.test(text)) return "email";
  if (/seo|search|google.*business|local.*search|ranking/.test(text)) return "seo";
  if (/restaurant|food|dining|menu|eat/.test(text)) return "food";
  if (/real.?estate|property|home|house/.test(text)) return "realestate";
  if (/dentist|medical|health|doctor|clinic/.test(text)) return "medical";
  if (/plumb|electric|trade|contractor|handyman/.test(text)) return "trades";
  if (/dooh|billboard|outdoor|programmatic|display|ctv|ott/.test(text)) return "programmatic";
  if (/local|store|shop|business|small/.test(text)) return "local";
  if (/adverti|campaign|brand|promotion/.test(text)) return "advertising";
  return "marketing";
}

function simpleHash(str) {
  let h = 5381;
  for (let i = 0; i < str.length; i++) h = ((h << 5) + h) ^ str.charCodeAt(i);
  return Math.abs(h);
}

function getPhotoUrl(slug, category) {
  const poolKey = detectPool(slug, category);
  const pool = POOL[poolKey];
  const idx = simpleHash(slug) % pool.length;
  return `https://images.unsplash.com/photo-${pool[idx]}?w=800&q=80&auto=format&fit=crop`;
}

// ─── UPDATE MDX FILES ──────────────────────────────────────────────────────
const files = fs.readdirSync(BLOG_DIR).filter((f) => f.endsWith(".mdx"));
let updated = 0;
const usageCount = {};

for (const file of files) {
  const filePath = path.join(BLOG_DIR, file);
  const content = fs.readFileSync(filePath, "utf8");

  const categoryMatch = content.match(/^category:\s*["']?(.+?)["']?\s*$/m);
  const category = categoryMatch ? categoryMatch[1].trim() : "";
  const slug = file.replace(".mdx", "");

  const newUrl = getPhotoUrl(slug, category);
  const photoId = newUrl.match(/photo-([^?]+)/)?.[1] ?? "unknown";
  usageCount[photoId] = (usageCount[photoId] ?? 0) + 1;

  const newContent = content.replace(
    /^image:\s*["'].*?["']\s*$/m,
    `image: "${newUrl}"`
  );

  if (newContent !== content) {
    fs.writeFileSync(filePath, newContent, "utf8");
    updated++;
  }
}

